-- this syntax is used for testing the HAMMING_DISTANCE() SQL function in HANA

WITH A AS (
	SELECT 'abcde' AS A1, 'abcde' AS A2 FROM DUMMY
	UNION
	select 'abcde', 'abcdf' FROM DUMMY
	UNION
	SELECT 'abcde', 'fdcba' from DUMMY
	UNION
	SELECT 'abcde', 'vwxyz' FROM DUMMY
	union
	SELECT 'abcde', 'a' FROM DUMMY
	UNION
	SELECT 'abcde', NULL FROM DUMMY
)
SELECT *, HAMMING_DISTANCE(A1, A2) AS HAMDIST FROM A;

SELECT
	HAMMING_DISTANCE('stuff', 'junk!'),
	HAMMING_DISTANCE('01110011 01110100 01110101 01100110 01100110', '01101010 01110101 01101110 01101011 00100001')
FROM DUMMY;